void CScene::BuildObjects(ID3D12Device *pd3dDevice, ID3D12GraphicsCommandList
	*pd3dCommandList)
{
	m_pd3dGraphicsRootSignature = CreateGraphicsRootSignature(pd3dDevice);
	//지형을 확대할 스케일 벡터이다. x-축과 z-축은 8배, y-축은 2배 확대한다.
	XMFLOAT3 xmf3Scale(8.0f, 2.0f, 8.0f);
	XMFLOAT4 xmf4Color(0.0f, 0.2f, 0.0f, 0.0f);
	//지형을 높이 맵 이미지 파일(HeightMap.raw)을 사용하여 생성한다. 높이 맵의 크기는 가로x세로(257x257)이다.
#ifdef _WITH_TERRAIN_PARTITION
	/*하나의 격자 메쉬의 크기는 가로x세로(17x17)이다. 지형 전체는 가로 방향으로 16개, 세로 방향으로 16의 격자 메
	쉬를 가진다. 지형을 구성하는 격자 메쉬의 개수는 총 256(16x16)개가 된다.*/
	m_pTerrain = new CHeightMapTerrain(pd3dDevice, pd3dCommandList,
		m_pd3dGraphicsRootSignature, _T("Terrain/MakeHeightMap.raw"), 257, 257, 17,
		17, xmf3Scale, xmf4Color);
#else
	//지형을 하나의 격자 메쉬(257x257)로 생성한다.
	m_pTerrain = new CHeightMapTerrain(pd3dDevice, pd3dCommandList,
		m_pd3dGraphicsRootSignature, _T("Terrain/MakeHeightMap.raw"), 200, 200, 200,
		200, xmf3Scale, xmf4Color);
#endif
	m_nShaders = 2;
	m_pShaders = new CObjectsShader[m_nShaders];

	m_pShaders[0].CreateShader(pd3dDevice, m_pd3dGraphicsRootSignature);
	m_pShaders[0].BuildWallObjects(pd3dDevice, pd3dCommandList, m_pTerrain);

	m_pShaders[1].CreateShader(pd3dDevice, m_pd3dGraphicsRootSignature);
	m_pShaders[1].BuildBallObjects(pd3dDevice, pd3dCommandList, m_pTerrain);

	BuildLightsAndMaterials();

	CreateShaderVariables(pd3dDevice, pd3dCommandList);
}


void CScene::ReleaseObjects()
{
	if (m_pd3dGraphicsRootSignature) m_pd3dGraphicsRootSignature->Release();
	for (int i = 0; i < m_nShaders; i++)
	{
		m_pShaders[i].ReleaseShaderVariables();
		m_pShaders[i].ReleaseObjects();
	}
	if (m_pShaders) delete[] m_pShaders;
	if (m_pTerrain) delete m_pTerrain;
}
void CScene::ReleaseUploadBuffers()
{
	for (int i = 0; i < m_nShaders; i++) m_pShaders[i].ReleaseUploadBuffers();
	if (m_pTerrain) m_pTerrain->ReleaseUploadBuffers();
}

void CScene::AnimateObjects(float fTimeElapsed, CPlayer& player)
{
	for (int i = 3; i < 11; i++) {
		if (m_pShaders[1].m_ppObjects[i]->m_isStatus == 0)
			m_pShaders[1].m_ppObjects[i]->m_isStatus = 1;

		else if (m_pShaders[1].m_ppObjects[i]->m_isStatus == 1) {
			if (m_pShaders[1].m_ppObjects[i]->GetPosition().z >= 920) {
				m_pShaders[1].m_ppObjects[i]->m_isStatus = 2;
				m_pShaders[1].m_ppObjects[i]->m_dirVector = { 0.0f, 0.0f, -0.05f };
				m_pShaders[1].m_ppObjects[i]->m_fRotationSpeed *= -1;
			}
		}
		else if (m_pShaders[1].m_ppObjects[i]->m_isStatus == 2) {
			if (m_pShaders[1].m_ppObjects[i]->GetPosition().z <= 880) {
				m_pShaders[1].m_ppObjects[i]->m_isStatus = 1;
				m_pShaders[1].m_ppObjects[i]->m_dirVector = { 0.0f, 0.0f, 0.05f };
				m_pShaders[1].m_ppObjects[i]->m_fRotationSpeed *= -1;
			}
		}
	}
	
	if (m_event == 0) {
		if (player.GetPosition().z > 810) {
			m_event = 1;
			m_pShaders[1].m_ppObjects[0]->m_isStatus = 1;
		}
	}
	else if (m_event == 1) {
		if (player.GetPosition().x > 840 && player.GetPosition().z < 820) {
			m_event = 2;
			m_pShaders[1].m_ppObjects[1]->m_isStatus = 1;
			m_pShaders[0].m_ppObjects[58]->m_isStatus = 1;
			m_pShaders[0].m_ppObjects[59]->m_isStatus = 1;

			//m_pShaders[0].m_ppObjects[58]->m_dirVector= {-0.1f, 0.0f, 0.0f};
			//m_pShaders[0].m_ppObjects[59]->m_dirVector= {-0.1f, 0.0f, 0.0f};
		}

	}
	else if (m_event == 2) {
		if (player.GetPosition().x > 870) {
			m_event = 3;
			m_pShaders[1].m_ppObjects[2]->m_isStatus = 1;
		}
	}

	if (m_pShaders[1].m_ppObjects[0]->GetPosition().x > 865)
		m_pShaders[1].m_ppObjects[0]->m_isStatus = 0;
				   
	if (m_pShaders[1].m_ppObjects[1]->GetPosition().x < 835)
		m_pShaders[1].m_ppObjects[1]->m_isStatus = 0;
	
	if (m_pShaders[1].m_ppObjects[2]->GetPosition().z > 910)
		m_pShaders[1].m_ppObjects[2]->m_isStatus = 0;

	if (m_pShaders[0].m_ppObjects[58]->m_isStatus) {
		m_pShaders[0].m_ppObjects[58]->SetPosition(m_pShaders[0].m_ppObjects[58]->GetPosition().x - 0.1, m_pShaders[0].m_ppObjects[58]->GetPosition().y + 0.1, m_pShaders[0].m_ppObjects[58]->GetPosition().z + 0.1);
		m_pShaders[0].m_ppObjects[59]->SetPosition(m_pShaders[0].m_ppObjects[59]->GetPosition().x - 0.1, m_pShaders[0].m_ppObjects[59]->GetPosition().y + 0.1, m_pShaders[0].m_ppObjects[59]->GetPosition().z - 0.1);
	}
	m_pShaders[0].m_ppObjects[90]->SetPosition(800, 100, 800);
	m_pShaders[0].m_ppObjects[91]->SetPosition(800, 100, 800);

	for (int i = 0; i < m_nShaders; i++)
	{
		m_pShaders[i].AnimateObjects(fTimeElapsed);
	}

#pragma region[플레이어X박스 충돌 ]

	for (int i = 0; i < m_pShaders[1].m_nObjects; i++)
	{
		if (m_pShaders[1].m_ppObjects[i]->m_isStatus) {

			if (m_pShaders[1].m_ppObjects[i]->m_xmOOBB.Intersects(player.m_xmOOBB))
			{
					PlaySound(L"effect_1.wav", NULL, SND_ASYNC);
			}
		}
	}
#pragma endregion
#pragma region[플레이어X벽 충돌 ]
	for (int i = 0; i < m_pShaders[0].m_nObjects; i++)
	{
		if (m_pShaders[0].m_ppObjects[i]->m_isStatus) {

			if (m_pShaders[0].m_ppObjects[i]->m_xmOOBB.Intersects(player.m_xmOOBB))
			{
				PlaySound(L"effect_1.wav", NULL, SND_ASYNC);
			}
		}
	}
#pragma endregion

	if (m_pLights)
	{
		m_pLights->m_pLights[1].m_xmf3Position = player.GetPosition();
		m_pLights->m_pLights[1].m_xmf3Direction = player.GetLookVector();
	}
}

